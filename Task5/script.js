// 2. Напишите цикл, который выводит на экран все числа от 1 до 100, которые делятся на 3 без остатка, на 5 без остатка и на 7 без остатка

console.log("start")
for (x = 0;x < 100; x++)
{
    if (x%3 == 0 || x%5 == 0 || x%7 == 0)
    {
        console.log("|\t" + x + "\t|");
    }
}
console.log("end")
